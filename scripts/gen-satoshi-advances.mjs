// Parse Satoshi-Bold.ttf and emit a table of horizontal advances (in
// pixels at 20px font size) for every character we care about. Read
// at runtime by `services/nameImage.ts:getFontSize` to estimate the
// rendered width of an ENS name without needing node-canvas.
//
// Kept out of the worker bundle — this script is strictly build-time.
// Regenerate whenever Satoshi-Bold.ttf changes:
//   `node scripts/gen-satoshi-advances.mjs`
import opentype from 'opentype.js'
import { readFile, writeFile } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')

const FONT_PATH = join(root, 'src/fonts/Satoshi-Bold.ttf')
const OUTPUT_PATH = join(root, 'src/fonts/satoshi-advances.ts')

const FONT_SIZE = 20

const font = opentype.parse((await readFile(FONT_PATH)).buffer)

// Covers every character that appears in a normalized `.eth` label
// (ASCII alphanumerics, dot, hyphen, punctuation) plus common Latin-1
// letters and a few symbols. Anything outside this set falls back to a
// per-script estimate in getFontSize.
const RANGES = [
  [0x20, 0x7e], // printable ASCII
  [0xa0, 0xff], // Latin-1 supplement
]

const advances = {}
for (const [lo, hi] of RANGES) {
  for (let cp = lo; cp <= hi; cp++) {
    const ch = String.fromCodePoint(cp)
    const glyph = font.charToGlyph(ch)
    if (!glyph || glyph.index === 0) continue // missing glyph
    const advancePx = (glyph.advanceWidth / font.unitsPerEm) * FONT_SIZE
    advances[ch] = Number(advancePx.toFixed(2))
  }
}

const banner = `// GENERATED by scripts/gen-satoshi-advances.mjs — do not edit.
// Horizontal advances in pixels at ${FONT_SIZE}px Satoshi-Bold.
// Regenerate with: node scripts/gen-satoshi-advances.mjs
`

const body = `export const SATOSHI_ADVANCES_20PX: Record<string, number> = ${JSON.stringify(advances, null, 2)} as const
`

await writeFile(OUTPUT_PATH, banner + body)
console.log(`wrote ${Object.keys(advances).length} advances to ${OUTPUT_PATH}`)
